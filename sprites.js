// Build a multi-color sprite from a pixel map
function buildColorSprite(pixelMap, colors, scale = SCALE) {
    const h = pixelMap.length;
    const w = pixelMap.length > 0 ? Math.max(...pixelMap.map(row => row.length)) : 0;

    const canvas = new OffscreenCanvas(w * scale, h * scale);
    const ctx = canvas.getContext('2d');

    for (let y = 0; y < pixelMap.length; y++) {
        const row = pixelMap[y];
        for (let x = 0; x < row.length; x++) {
            const ch = row[x];
            if (ch === '.') continue;
            const idx = parseInt(ch) - 1;
            if (idx >= 0 && idx < colors.length) {
                const [r, g, b] = colors[idx];
                ctx.fillStyle = `rgb(${r}, ${g}, ${b})`;
                ctx.fillRect(x * scale, y * scale, scale, scale);
            }
        }
    }
    return canvas;
}

