// Set of sprite names that are custom multi-color (bypass theme coloring)
const CUSTOM_SPRITE_NAMES = new Set([
    "player_purple_devil", "player_purple_devil_open",
    "player_double_blastery", "player_red_bomber",
    "player_phoenix",
    "bullet_red_pulse",
    "drone", "swarm_drone", "slime_glob", "dragon",
]);

// Sprite Manager class for managing all sprites and themes
class SpriteManager {
    constructor() {
        this.cache = new Map();  // key: "name_r_g_b"
        this.currentThemeIdx = 0;
        this.theme = { ...THEMES[0] };
    }

    setTheme(idx) {
        this.currentThemeIdx = idx % THEMES.length;
        this.theme = { ...THEMES[this.currentThemeIdx] };
        this.cache.clear();
    }

    nextTheme() {
        this.setTheme(this.currentThemeIdx + 1);
    }

    setLevelTheme(themeDict) {
        this.theme = { ...themeDict };
        this.cache.clear();
    }

    resetToDefault() {
        this.setTheme(0);
    }

    get fgColor() {
        return this.theme["hud"];
    }

    get bgColor() {
        return this.theme["bg"];
    }

    setColors(fg, bg) {
        this.theme["hud"] = fg;
        this.theme["bg"] = bg;
        this.cache.clear();
    }

    getColorForSprite(name) {
        const t = this.theme;
        if (name.startsWith("player_")) {
            return t["player"];
        } else if (name.startsWith("bullet_") || name.startsWith("homing")) {
            return t["player_bullet"];
        } else if (name.startsWith("ebullet_") || name.startsWith("guided_") ||
                   name.startsWith("arrow_") || name.startsWith("missile") ||
                   name.startsWith("enemy_bomb")) {
            return t["enemy_bullet"];
        } else if (name.startsWith("explosion_") || name.startsWith("large_explosion_") ||
                   name.startsWith("lexplosion")) {
            return t["explosion"];
        } else if (name.startsWith("cash_")) {
            return t["player_bullet"];
        } else {
            return t["enemy"];
        }
    }

    get(name) {
        // Custom multi-color sprites bypass theme coloring
        if (CUSTOM_SPRITE_NAMES.has(name)) {
            const custom = getCustomSprite(name);
            if (custom) return custom;
        }

        const color = this.getColorForSprite(name);
        const key = `${name}_${color[0]}_${color[1]}_${color[2]}`;

        if (!this.cache.has(key)) {
            if (name in RAW_SPRITES) {
                const pixels = parseSpriteData(RAW_SPRITES[name]);
                const canvas = createOffscreenCanvas(pixels, SCALE, color);
                this.cache.set(key, canvas);
            } else {
                console.warn(`WARNING: Missing sprite '${name}', using placeholder`);
                const canvas = new OffscreenCanvas(8 * SCALE, 8 * SCALE);
                const ctx = canvas.getContext('2d');
                const [r, g, b] = color;
                ctx.strokeStyle = `rgb(${r}, ${g}, ${b})`;
                ctx.lineWidth = 2;
                ctx.strokeRect(0, 0, 8 * SCALE, 8 * SCALE);
                this.cache.set(key, canvas);
            }
        }
        return this.cache.get(key);
    }

    getSize(name) {
        const canvas = this.get(name);
        return [canvas.width / SCALE, canvas.height / SCALE];
    }
}

// Create a global sprite manager instance
const spriteManager = new SpriteManager();
