// Phoenix PC Port - Sprite data and rendering
// All sprites extracted from sprites.asm (binary pixel data from TI-89 version)

// Parse binary sprite data from 32-bit ints
function parseSpriteData(rows) {
    const pixels = [];
    for (const row of rows) {
        const line = [];
        for (let bit = 23; bit >= 7; bit--) {
            line.push((row >> bit) & 1);
        }
        pixels.push(line);
    }
    return pixels;
}

// Trim empty columns from sprite data
function trimSprite(pixels) {
    if (!pixels || pixels.length === 0) {
        return [pixels, 0];
    }
    let maxCol = 0;
    let minCol = pixels[0].length;
    for (const row of pixels) {
        for (let c = 0; c < row.length; c++) {
            if (row[c]) {
                minCol = Math.min(minCol, c);
                maxCol = Math.max(maxCol, c);
            }
        }
    }
    if (minCol > maxCol) {
        return [[[0]], 1];
    }
    const trimmed = pixels.map(row => row.slice(minCol, maxCol + 1));
    return [trimmed, maxCol - minCol + 1];
}

// Create OffscreenCanvas from pixel array
function createOffscreenCanvas(pixels, scale = SCALE, fg = null, bg = null) {
    if (fg === null) {
        fg = COLOR_FG;
    }
    if (bg === null) {
        bg = [0, 0, 0, 0];  // transparent
    }
    const [trimmedPixels, width] = trimSprite(pixels);
    const height = trimmedPixels.length;

    const canvas = new OffscreenCanvas(width * scale, height * scale);
    const ctx = canvas.getContext('2d');

    for (let y = 0; y < trimmedPixels.length; y++) {
        const row = trimmedPixels[y];
        for (let x = 0; x < row.length; x++) {
            if (row[x]) {
                const [r, g, b] = fg;
                ctx.fillStyle = `rgb(${r}, ${g}, ${b})`;
                ctx.fillRect(x * scale, y * scale, scale, scale);
            }
        }
    }
    return canvas;
}

